int	ft_iterative_power(int nb, int power)
{
	int	result;
	int	iterator;

	result = 1;
	iterator = 0;
	if (nb == 0 && power == 0)
		return (1);
	if (power == 0)
		return (1);
	if (power < 0)
		return (0);
	result = nb;
	while (iterator < power - 1)
	{
		result = result * nb;
		iterator++;
	}
	return (result);
}
